public class BoundingBox {
  double xMin;
  double xMax;
  double yMin;
  double yMax;

  @Override
  public String toString() {
    return "BoundingBox{"
        + "xMin="
        + xMin
        + ", xMax="
        + xMax
        + ", yMin="
        + yMin
        + ", yMax="
        + yMax
        + '}';
  }
}
